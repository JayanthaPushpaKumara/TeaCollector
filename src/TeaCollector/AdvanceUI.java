    private void btn_delete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete1ActionPerformed
        if (AdvId != 0) {
            try {
                advanceJpaController.destroy(AdvId);
                txt_amount1.setText("");
                txt_supID1.setText("");
                spin_NoOfMnt.setValue(Integer.valueOf("1"));                
                lbl_name.setText("");
                AdvId = 0;
                JOptionPane.showMessageDialog(null, "Data Deleted..");                
                pnl_recordComponentShown(null);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Cant delete.." + e);
            }

        } else {
            JOptionPane.showMessageDialog(null, "AdvId = 0");
        }
    }//GEN-LAST:event_btn_delete1ActionPerformed

    private void btn_add1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add1ActionPerformed
        // TODO add your handling code here:
        Advance advance = new Advance();

        advance.setSupplierID(supplierJpaController.findSupplier(Integer.parseInt(txt_supID1.getText())));
        advance.setAmount(Integer.parseInt(txt_amount1.getText()));
        advance.setStartMonth(date_start1.getDate());

        advance.setTimeMonth((Integer) spin_NoOfMnt.getValue());
        advance.setPaid(0);
        advance.setStatus(true);

        try {
            advanceJpaController.create(advance);
            JOptionPane.showMessageDialog(null, "Data seved..");
            txt_amount1.setText("");
            txt_supID1.setText("");
            spin_NoOfMnt.setValue(Integer.valueOf("1"));
            lbl_name.setText("");
            pnl_recordComponentShown(null);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Somthing wrong... " + e);
        }
    }//GEN-LAST:event_btn_add1ActionPerformed

