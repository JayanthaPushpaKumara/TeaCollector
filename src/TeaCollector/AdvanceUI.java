    private void pnl_viwAdvComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_pnl_viwAdvComponentShown
        
        List<entity.Advance> advanceEntity = advanceJpaController.findAdvanceEntities();
        DefaultTableModel model = (DefaultTableModel) tbl_viewAll.getModel();
        model.setRowCount(0);
        Object[] row = new Object[7];
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");

        for (int i = 0; i < advanceEntity.size(); i++) {

            row[0] = advanceEntity.get(i).getAdvId();
            row[1] = advanceEntity.get(i).getSupplierID().getId();
            row[2] = advanceEntity.get(i).getAmount();
            row[3] = advanceEntity.get(i).getPaid();
            row[4] = Integer.parseInt(row[2].toString()) - Integer.parseInt(row[3].toString());
            row[5] = simpleDateFormat.format(advanceEntity.get(i).getStartMonth());
            row[6] = advanceEntity.get(i).getTimeMonth();

            model.addRow(row);

        }
    }//GEN-LAST:event_pnl_viwAdvComponentShown

